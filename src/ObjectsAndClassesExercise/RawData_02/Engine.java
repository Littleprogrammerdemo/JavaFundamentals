package ObjectsAndClassesExercise.RawData_02;
public class Engine {
    public int speed;
    public int power;

    public Engine(int speed, int power) {
        this.speed = speed;
        this.power = power;
    }

    public int getSpeed() {
        return speed;
    }

    public int getPower() {
        return power;
    }
}
